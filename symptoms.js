/* เครื่องมือประเมิน: รับ id อาการที่เลือก คืนผลสรุป */
function evaluateSymptoms(selectedIds){
  const all = [];
  SYMPTOM_GROUPS.forEach(g=>g.symptoms.forEach(s=>{ if(selectedIds.includes(s.id)) all.push(s); }));
  if(!all.length) return null;
  const maxU = Math.max(...all.map(s=>s.u));
  const hasRedFlag = all.some(s=>s.rf);
  const level = hasRedFlag ? 3 : maxU;
  // รวมภาวะที่อาจเกี่ยวข้อง + นับความถี่
  const condCount = {};
  all.forEach(s=>s.cond.forEach(c=>condCount[c]=(condCount[c]||0)+1));
  const conditions = Object.entries(condCount).sort((a,b)=>b[1]-a[1]).map(x=>x[0]);
  const advice = all.map(s=>"• "+s.advice).filter((v,i,a)=>a.indexOf(v)===i);
  const labels = all.map(s=>s.label);
  const activeRedFlags = all.filter(s=>s.rf).map(s=>s.label);
  return { level, conditions, advice, labels, activeRedFlags, count:all.length };
}

